    public static function getMiddlegameBonus(int $square, int $pieceType, int $color): int {
        $idx = $color === \CHESS_WHITE ? $square : 63 - $square;
        
        return match($pieceType) {
            \CHESS_PAWN => self::PAWN_MG[$idx],
            \CHESS_KNIGHT => self::KNIGHT_MG[$idx],
            \CHESS_BISHOP => self::BISHOP_MG[$idx],
            \CHESS_ROOK => self::ROOK_MG[$idx],
            \CHESS_QUEEN => self::QUEEN_MG[$idx],
            \CHESS_KING => self::KING_MG[$idx],
            default => 0,
        };
    }

    public static function getEndgameBonus(int $square, int $pieceType, int $color): int {
        $idx = $color === \CHESS_WHITE ? $square : 63 - $square;
        
        return match($pieceType) {
            \CHESS_PAWN => self::PAWN_EG[$idx],
            \CHESS_KNIGHT => self::KNIGHT_EG[$idx],
