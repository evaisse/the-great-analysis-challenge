export function getMiddlegameBonus(square: Square, pieceType: PieceType, color: Color): number {
  const idx = color === "white" ? square : 63 - square;
  switch (pieceType) {
    case "P": return PAWN_MG[idx];
    case "N": return KNIGHT_MG[idx];
    case "B": return BISHOP_MG[idx];
    case "R": return ROOK_MG[idx];
    case "Q": return QUEEN_MG[idx];
    case "K": return KING_MG[idx];
  }
}

export function getEndgameBonus(square: Square, pieceType: PieceType, color: Color): number {
  const idx = color === "white" ? square : 63 - square;
  switch (pieceType) {
    case "P": return PAWN_EG[idx];
    case "N": return KNIGHT_EG[idx];
    case "B": return BISHOP_EG[idx];
    case "R": return ROOK_EG[idx];
    case "Q": return QUEEN_EG[idx];
    case "K": return KING_EG[idx];
  }
}
