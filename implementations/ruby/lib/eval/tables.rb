      def self.get_middlegame_bonus(square, piece_type, color)
        idx = color == :white ? square : 63 - square
        case piece_type
        when :pawn then PAWN_MG[idx]
        when :knight then KNIGHT_MG[idx]
        when :bishop then BISHOP_MG[idx]
        when :rook then ROOK_MG[idx]
        when :queen then QUEEN_MG[idx]
        when :king then KING_MG[idx]
        else 0
        end
      end

      def self.get_endgame_bonus(square, piece_type, color)
